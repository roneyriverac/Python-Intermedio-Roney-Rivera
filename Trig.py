#Importación de librerías

import math

#Creación de la clase Trig con sus respectivos atributos y métodos. 
class Trig:
    #Atributo de clase
    pi = math.pi

    #Métodos de Trig
    def seno(self):
        return math.sin(self.pi)

    def coseno(self):
        return math.cos(self.pi)

    def tangente(self):
        return math.tan(self.pi)



       
    
        
    
        

    
        

        

    

    

        
        
        
    





